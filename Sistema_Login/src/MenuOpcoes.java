    private void ctxVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ctxVisualizarActionPerformed
        
        
        File ficheiro = new File (Login.log+".txt");
    
        if (!ficheiro.exists()){
            System.out.println("O utilizador nÃ£o existe");
            
        } else{
            try{
                FileReader fr = new FileReader(ficheiro);
                BufferedReader br = new BufferedReader(fr);
                while (br.ready()){
                    String linha = br.readLine();
                    System.out.println(linha);
                }
                br.close();
                fr.close();
            }catch (FileNotFoundException ex){
                ex.printStackTrace();
            } catch (IOException ioe){
            }
        
         }
    }//GEN-LAST:event_ctxVisualizarActionPerformed

