    public static boolean validaCampoPass(String pass) {
        int x = 0, p = pass.length();
        int mai = 0;
        int min = 0;
        int num = 0;
        int sc = 0;
        if (p < 8)
            return false;
        for (x = 0; x < p; x++){
            char c = pass.charAt(x);
            if (c == ' ')
                return false;
            if (Character.isDigit(c)){
                num++;
            }
            if (Character.isUpperCase(c)){
                mai++;
            }
            if (Character.isLowerCase(c)){
                min++;
            }
            if (!isDigit(c) && !isLetter(c))
                sc++;
    
         }
    if (min > 0 && mai> 0 && sc > 0 && num > 0)
        return true;
    else
         return false;
    }
