    public static boolean validaCampoNumerico(String telefone) {
        int x,contador = 0,t = telefone.length();
        char c;
        if(t !=9)
            return false;
        else{
            for (x = 0; x<t; x++){
                c = telefone.charAt(x);
                if (isDigit(c))
                    contador++; 
            }
            if (t!=contador)
                return false;
        }
        return true;
    }

    public static boolean validaCampoNum(String nome) {
        int x, contador = 0, n = nome.length();
        char c = 0;
        if (n <2)
            return false;
        else{
            for (x = 0; x<n; x++){
                c = nome.charAt(x);
                if (isLetter(c))
                    contador++;
            }
            if (n!=contador)
                return false;
        }
        return true;
    }

    public  static boolean validaCampoMorada(String morada) {
        int x, contador = 0, m = morada.length();
        char c = 0;
        if (m <5)
            return false;
        else{
            for (x = 0; x<m; x++){
                c = morada.charAt(x);
                if (isLetter(c))
                    contador++;
            }
            if (m!=contador)
                return false;
        }
        return true;
    }

    public static boolean validaCampoEmail(String email) {
        
        if (email.indexOf('@') >-1 && email.indexOf('.') >-1)
            return true;
        else
            return false;
    }
